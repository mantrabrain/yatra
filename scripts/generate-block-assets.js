/**
 * Generate .asset.php files for WordPress blocks
 * These files tell WordPress what dependencies each block needs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const blocksDir = path.resolve(__dirname, '../assets/dist/blocks');

const blocks = ['tour', 'activity', 'destination'];

const dependencies = [
    'wp-blocks',
    'wp-element',
    'wp-block-editor',
    'wp-components',
    'wp-i18n'
];

blocks.forEach(blockName => {
    const assetContent = `<?php
/**
 * Asset file for ${blockName} block
 * Auto-generated by build process
 */
return array(
    'dependencies' => array(
        ${dependencies.map(dep => `'${dep}'`).join(',\n        ')}
    ),
    'version' => '${Date.now()}'
);
`;

    const assetPath = path.join(blocksDir, `${blockName}.asset.php`);
    fs.writeFileSync(assetPath, assetContent);
    console.log(`✓ Generated ${blockName}.asset.php`);
});

console.log('\n✅ All block asset files generated successfully!');
